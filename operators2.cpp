// Relation Operators

#include<iostream>
using namespace std;
int main(){
    int x=10;
    int y=10;
    cout<<(x==y)<<endl; //1
    cout<<(x!=y)<<endl; //0
    cout<<(x>y)<<endl;  //0
    cout<<(x<y)<<endl;  //0
    cout<<(x>=y)<<endl; //1
    cout<<(x<=y)<<endl; //1
    return 0;
}